"""Minimal native executable writers for Phase 20/28 bootstrap evidence."""

from __future__ import annotations

from pathlib import Path

from .native_backends import TargetTriple


def write_minimal_native_executable(path: Path, *, target: TargetTriple) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    if target.os == "windows":
        path.write_bytes(_MINIMAL_PE64)
    elif target.os == "linux":
        path.write_bytes(_MINIMAL_ELF64)
    else:
        path.write_bytes(_MINIMAL_MACHO64)
    return path


